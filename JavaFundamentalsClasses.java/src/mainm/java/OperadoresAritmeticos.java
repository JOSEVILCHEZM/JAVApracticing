
// SUMAR + = a + b -> 12
// RESTAR - = a - b -> 8
// MULTIPLICAR * = a * b -> 20
// DIVIDIR / = a / b -> 5
// RESTO DE UNA DIVISIÓN % = a % b -> 0

public class OperadoresAritmeticos {
    
    public static void main(String [] args){
        int a = 63; // VARIABLES A ES IGUAL A 10
        int b = 2;
        int c = 5;
        
        System.out.println(63 % 2);
        System.out.println(a - b * c);
        
        // SUMA en Asignación
        a = 10;
        a+= 5;
        System.out.println(a);
    }
       
    
}
